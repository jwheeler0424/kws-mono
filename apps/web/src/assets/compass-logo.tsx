import type { ComponentProps, CSSProperties } from 'react';

export type LogoVariant = 'logo' | 'symbol';
export type LogoTheme = 'light' | 'dark';
export type LogoStyle = 'ghost' | 'solid';

export type CompassLogoProps = ComponentProps<'svg'> & {
  variant?: LogoVariant; // default is "logo"
  theme?: LogoTheme; // default is "dark"
  style?: LogoStyle; // default is "solid"
};

export function CompassLogo({ className, variant, ...props }: CompassLogoProps) {
  switch (variant) {
    case 'logo':
      return <LogoStyle className={className} {...props} />;
    case 'symbol':
      return <LogoStyleSymbol className={className} {...props} />;
    default:
      return <LogoStyle className={className} {...props} />;
  }
}

type LogoStyleProps = ComponentProps<'svg'> & {
  theme?: LogoTheme;
  style?: LogoStyle;
};

function LogoStyle({ className, style, theme, fill, fillRule }: LogoStyleProps) {
  const bgFillColor = theme === 'light' ? fill || '#000' : fill || '#fff';
  const fillColor = theme === 'light' ? fill || '#FFF' : fill || '#000';
  return style === 'ghost' ? (
    <LogoTheme className={className} fill={fill} fillRule={fillRule} theme={theme} />
  ) : (
    <div
      style={{ '--background-color': bgFillColor } as CSSProperties}
      className={'bg-(--background-color) invert'}>
      <LogoTheme className={className} fill={fillColor} fillRule={fillRule} theme={theme} />
    </div>
  );
}

